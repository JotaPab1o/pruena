    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        
        
      //  String date = datenacimieno.getDateFormatString();
       String ins="INSERT INTO usuarios (nombre_usuario, apellido_usuario,fecha_nac,telefono, mail, observacion, usuario, contrasena, fk_idroles) VALUES(?,?,?,?,?,?,?,?,?)";
       try {
            PreparedStatement pst = cn.prepareStatement(ins);
            pst.setString(1, txtnombre.getText());
            pst.setString(2, txtapellido.getText());
            pst.setDate(3, new java.sql.Date(datenacimiento.getDate().getTime()));
            pst.setString(4, txtfono.getText());
            pst.setString(5, txtmail.getText());
            pst.setString(6, txtareaNota.getText());
            pst.setString(7, txtusuario.getText());
            pst.setString(8,new String(txtpassword.getPassword()));
            pst.setInt(9, cboxcargo.getSelectedIndex());
                int n= pst.executeUpdate();
                if(n>0)
            {
                JOptionPane.showMessageDialog(this, "Se guardaron los datos");
        //        mostrarusuarios();
                txtnombre.setText(null);
                txtapellido.setText(null);
                datenacimiento.setDate(null);
                txtfono.setText(null);
                txtmail.setText(null);
                txtareaNota.setText(null);
                txtusuario.setText(null);
                txtpassword.setText(null);
                cboxcargo.setSelectedIndex(0);
                
                
        //        Generarnumeracion();
            }
            else
            {
                 JOptionPane.showMessageDialog(this, "Error");
            }
            
            
        } catch (SQLException ex) {
            Logger.getLogger(Creacionusuario.class.getName()).log(Level.SEVERE, null, ex);
            
        }
      
       
    }//GEN-LAST:event_btnguardarActionPerformed

